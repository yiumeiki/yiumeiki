void	rush(int x, int y)
{
	int	p;
	int	r;

	r = 0;
	while (r < y && x > 0)
	{
		p = 0;
		while (p < x)
		{
			if ((p != 0 && p != (x - 1)) && (r == 1 || r == (y - 1)))
				ft_putchar('-');
			else if ((r != 0 && r != (y - 1)) && (p == 0 || p == (x - 1)))
				ft_putchar('|');
			else if ((p == 0) || (p == (x - 1)) || (r == 0) || (r == (y - 1)))
				ft_putchar('o');
			else
				ft_putchar(' ');
			p++;
		}
		ft_putchar('\n');
		r++;
	}
}
