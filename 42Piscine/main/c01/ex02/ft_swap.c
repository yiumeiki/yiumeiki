#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int	x;
	int	y;
	int	*a;
	int	*b;

	x = 2;
	y = 5;
	a = &x;
	b = &y;
	ft_swap(a, b);
	printf("%d, %d\n", x, y);
	return (0);
}
